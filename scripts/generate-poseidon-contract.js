#!/usr/bin/env node

/**
 * Generate Poseidon Solidity contracts from circomlibjs
 * This creates PoseidonT2 (1 input) and PoseidonT3 (2 inputs) contracts
 */

import { buildPoseidon } from "circomlibjs";
import { createCode, generateABI } from "circomlibjs/src/poseidon_gencontract.js";
import fs from "fs";
import path from "path";
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function generateContract(nInputs, outputName) {
    console.log(`Generating ${outputName} with ${nInputs} input(s)...`);

    const bytecode = createCode(nInputs);
    const abi = generateABI(nInputs);

    // Generate Solidity contract wrapper
    const solidityCode = `// SPDX-License-Identifier: LGPL-3.0-only
// WARNING: This is an autogenerated file. Do not edit manually.
// Generated by circomlibjs poseidon_gencontract.js
pragma solidity ^0.8.13;

/**
 * @title ${outputName}
 * @notice Poseidon hash function with ${nInputs} input(s)
 * @dev Generated from circomlibjs to match circomlib Poseidon circuit
 */
library ${outputName} {
    function poseidon(uint256[${nInputs}] memory input) public pure returns (uint256) {
        // This is a precompiled contract at a specific address
        // or inline assembly implementation
        // For now, using delegatecall to deployed bytecode

        // In production, deploy the bytecode and call it
        // Or use inline assembly with the generated bytecode

        // Placeholder - needs actual implementation
        assembly {
            // TODO: Insert generated bytecode or call to deployed contract
        }

        // Temporary: return 0 (will be replaced with actual implementation)
        return 0;
    }
}
`;

    return {
        solidityCode,
        bytecode,
        abi,
        name: outputName
    };
}

async function main() {
    console.log("Generating Poseidon contracts...\n");

    // Generate PoseidonT2 (1 input) and PoseidonT3 (2 inputs)
    const contracts = [
        await generateContract(1, "PoseidonT2"),
        await generateContract(2, "PoseidonT3")
    ];

    // Create output directory
    const outputDir = path.join(__dirname, "..", "contracts", "src", "generated");
    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    // Write contracts
    for (const contract of contracts) {
        const solidityPath = path.join(outputDir, `${contract.name}.sol`);
        const bytecodePath = path.join(outputDir, `${contract.name}.bytecode.txt`);
        const abiPath = path.join(outputDir, `${contract.name}.abi.json`);

        fs.writeFileSync(solidityPath, contract.solidityCode);
        fs.writeFileSync(bytecodePath, contract.bytecode);
        fs.writeFileSync(abiPath, JSON.stringify(contract.abi, null, 2));

        console.log(`✅ Generated ${contract.name}`);
        console.log(`   - Solidity: ${solidityPath}`);
        console.log(`   - Bytecode: ${bytecodePath}`);
        console.log(`   - ABI: ${abiPath}\n`);
    }

    console.log("✨ Done! Next steps:");
    console.log("1. Review the generated contracts in contracts/src/generated/");
    console.log("2. The bytecode needs to be deployed or included via assembly");
    console.log("3. Consider using @iden3/contracts package for a ready-made solution");
    console.log("\nAlternative approach:");
    console.log("  npm install @iden3/contracts");
    console.log("  Then import: import {PoseidonT3} from '@iden3/contracts/lib/Poseidon.sol';");
}

main().catch(console.error);
